void clear_matrix(_Bool array[32][32]) //clean current compass matrix
{
  int i,j;
  for(i=0; i<32; i++)
    for(j=0; j<32; j++)
    {
       array[i][j]=0;
    }

}

void rotation(double radian, _Bool array[32][32], const _Bool orig_array[32][32])   // function for rotating the north matrix about the middle/origin for X radians
{                              // and storing it in compass matrix
  double x,y;
  double rx,ry; //rotated x and y
  int i,j;
  int ri,rj; //rotated i and j
  for(i=0; i<32; i++)
    for(j=0; j<32; j++)
    {
      if(orig_array[i][j]==0) //skip 0 values since we care only about 1s
      continue;
      x=(double) -15 + j; //convert i into x coordinates (origin=[15][15])
      y=(double) 15 - i; //convert j into y coordinates
      rx=cos(radian)*x+sin(radian)*y; //find rotated coordinates
      ry=-sin(radian)*x+cos(radian)*y; //find rotated coordinates
      rx=(int)(rx < 0 ? (rx - 0.5) : (rx + 0.5));
      ry=(int)(ry < 0 ? (ry - 0.5) : (ry + 0.5)); //rounding double to int
      rj=15+rx;
      ri=15-ry; //getting rotated matrix coordinates
      array[ri][rj]=1; //setting the rotated bit in the final matrix to 1
    }
  }

void fix_matrix(_Bool array[32][32]){ //deletes bit gaps
  int i,j,k;
  for(i=0; i<32; i++){
    for(j=0; j<32; j++)
    {
      if(array[i-1][j]&&array[i+1][j]||array[i][j-1]&&array[i][j+1]) //checking if there are any gaps
        array[i][j]=1;                                                     //and filling them with 1s
      if(array[i-1][j]&&array[i+2][j]||array[i-2][j]&&array[i+1][j]) //check for wider vertical gaps
        { array[i][j]=1;
          array[i+1][j]=1;
          array[i-1][j]=1;}
      if(array[i][j+1]&&array[i][j-2]||array[i][j-1]&&array[i][j+2]) //check for wider horizontal gaps
        { array[i][j]=1;
          array[i][j+1]=1;
          array[i][j-1]=1;}

    }
  }
}
int rot=0; //variable for the humorous rotating triangle page
void increase_rot(void) //function for increasing rotation
{
  rot++;
}

void convert_to_data(uint8_t output[128],_Bool array[32][32]) //converts the 32x32 bitmap into 4 lines of 32 vertical bytes ready to be printed on the display
{
  int i,j,k;
  for(i=0;i<4;i++) //splits data into 4 lines
    for(j=0;j<32;j++) //goes through every single byte
      for(k=8;k>0;k--) //converts single bits of the array into one byte
      {
        output[j+i*32]=output[j+i*32]<<1; //shift the current value of the byte by 1 (multiply by 2)
        output[j+i*32]=output[j+i*32]+(1*array[i*8+k][j]); //add (1*array bit) to the current value
      }
}
void compasswork(void) //function for spinning triangle
{
  clear_matrix(compass);
  rotation((double)rot/10,compass,north);
  increase_rot();
  fix_matrix(compass);
  convert_to_data(compass_data,compass);
  display_image(96,compass_data);
}

